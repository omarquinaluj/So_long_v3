#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	number;
	int	signe;
	int	i;

	i = 0;
	number = 0;
	signe = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			signe *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		number *= 10;
		number += (nptr[i] - '0');
		i++;
	}
	return (number * signe);
}
